int	ft_count(const char *s, char c)
{
	int	i;
	int	words;

	i = 0;
	words = 0;
	if (s[i] != c && s[i] != '\0')
	{
		words++;
		i++;
	}
	while (s[i] != '\0')
	{
		while (s[i] == c && s[i] != '\0')
		{
			i++;
			if (s[i] != c && s[i] != '\0')
				words++;
		}
		i++;
	}
	return (words);
}
