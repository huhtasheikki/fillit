#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	while (s1[i] != '\0')
		i++;
	j = 0;
	while (s2[j] != '\0' && n > j)
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
